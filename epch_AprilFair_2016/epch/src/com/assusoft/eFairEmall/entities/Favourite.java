package com.assusoft.eFairEmall.entities;

public class Favourite {
    
    int id,exhibitorId,visitorId,status;
    String creationDate,updatedBy;

    public Favourite() {
    }

    public Favourite(int id, int exhibitorId, int visitorId, String creationDate, String updatedBy) {
        this.id = id;
        this.exhibitorId = exhibitorId;
        this.visitorId = visitorId;
        this.creationDate = creationDate;
        this.updatedBy = updatedBy;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getExhibitorId() {
        return exhibitorId;
    }

    public void setExhibitorId(int exhibitorId) {
        this.exhibitorId = exhibitorId;
    }

    public int getVisitorId() {
        return visitorId;
    }

    public void setVisitorId(int visitorId) {
        this.visitorId = visitorId;
    }

    public String getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(String creationDate) {
        this.creationDate = creationDate;
    }

    public String getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(String updatedBy) {
        this.updatedBy = updatedBy;
    }

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
    
}
