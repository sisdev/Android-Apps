package com.assusoft.eFairEmall.entities;

public class Feedback {
    int id,status;
    String emailId,userInterface,dataAccuracy,performance,easeOfuse,remarks;
    String creationDate,updatedBy;

    public Feedback() {
    }

    public Feedback(int id, String emailId, String userInterface, String dataAccuracy, String performance, String easeOfuse, String remarks, String creationDate, String updatedBy) {
        this.id = id;
        this.emailId = emailId;
        this.userInterface = userInterface;
        this.dataAccuracy = dataAccuracy;
        this.performance = performance;
        this.easeOfuse = easeOfuse;
        this.remarks = remarks;
        this.creationDate = creationDate;
        this.updatedBy = updatedBy;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmailId() {
        return emailId;
    }

    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }

    public String getUserInterface() {
        return userInterface;
    }

    public void setUserInterface(String userInterface) {
        this.userInterface = userInterface;
    }

    public String getDataAccuracy() {
        return dataAccuracy;
    }

    public void setDataAccuracy(String dataAccuracy) {
        this.dataAccuracy = dataAccuracy;
    }

    public String getPerformance() {
        return performance;
    }

    public void setPerformance(String performance) {
        this.performance = performance;
    }

    public String getEaseOfuse() {
        return easeOfuse;
    }

    public void setEaseOfuse(String easeOfuse) {
        this.easeOfuse = easeOfuse;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public String getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(String creationDate) {
        this.creationDate = creationDate;
    }

    public String getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(String updatedBy) {
        this.updatedBy = updatedBy;
    }

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
    
    
}
